//JavaScript Operators

//Basic Operators

let inbox = 10;

console.log(inbox + 5);
console.log(inbox - 5);
console.log(inbox * 5);
console.log(inbox / 5);
console.log(inbox ** 2);
console.log(inbox ** 3);
console.log(inbox % 3);
console.log(inbox++);
console.log(inbox--);
console.log(--inbox);
console.log(++inbox);
inbox += 20; // same as inbox = inbox + 20
console.log(inbox);
